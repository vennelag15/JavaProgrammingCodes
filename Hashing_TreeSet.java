import java.util.*;
public class Hashing_TreeSet {
    public static void main(String args[]){
HashSet<String>cities=new HashSet<>();
      cities.add("delhi");
      cities.add("mumbai");
      cities.add("noida");
      cities.add("bangalore");
      System.out.println(cities);
      TreeSet<String>ts=new TreeSet<>();
      ts.add("delhi");
      ts.add("mumbai");
      ts.add("noida");
      ts.add("bangalore");
      System.out.println(ts);


    }

    
}
